package it.solvingteam.paddle.dto;

public class CampoDTO {
	
	private String id;
	
	private String nome;
	
	private String circoloId;
	
	private String aperto;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCircoloId() {
		return circoloId;
	}

	public void setCircoloId(String circoloId) {
		this.circoloId = circoloId;
	}

	public String getAperto() {
		return aperto;
	}

	public void setAperto(String aperto) {
		this.aperto = aperto;
	}

}
