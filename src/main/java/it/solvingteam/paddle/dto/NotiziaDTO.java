package it.solvingteam.paddle.dto;

public class NotiziaDTO {
	
	private String id;
	
	private String messagio;
	
	private String data;
	
	private String circoloId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMessagio() {
		return messagio;
	}

	public void setMessagio(String messagio) {
		this.messagio = messagio;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getCircoloId() {
		return circoloId;
	}

	public void setCircoloId(String circoloId) {
		this.circoloId = circoloId;
	}

}
